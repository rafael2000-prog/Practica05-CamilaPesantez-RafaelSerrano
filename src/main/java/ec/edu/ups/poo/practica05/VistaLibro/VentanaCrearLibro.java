    private void agregarAlCombo() {
        ComboBoxBibliotecas.removeAllItems();

    List<Biblioteca> bibliotecas = bibliotecaControlador.list();
    for (Biblioteca biblioteca : bibliotecas) {
        ComboBoxBibliotecas.addItem(biblioteca.getNombre());
    }
    
    }
