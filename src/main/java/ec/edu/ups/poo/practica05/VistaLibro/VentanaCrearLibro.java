    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        try {
        String Titulo = txtTituto.getText();
        String Autor = txtAutor.getText();
        int Año = Integer.parseInt(txtAño.getText());

        if (libroControlador.read(Titulo) == null) {
            libroControlador.create(Titulo, Autor, Año, "SI");
            Libro libro = libroControlador.read(Titulo);
            Biblioteca biblioteca = bibliotecaControlador.read(Año);
            biblioteca.agregarLibros(libro);

            JOptionPane.showMessageDialog(this, "Se ha creado con éxito ;)");
            agregarAlCombo();
        } else {
            JOptionPane.showMessageDialog(this, "Ese libro ya existe!");
        }

        txtTituto.setText("");
        txtAutor.setText("");
        txtAño.setText("");
        
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Por favor, ingresa un año válido.");
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Ha ocurrido un error al guardar el libro.");
    }
    }//GEN-LAST:event_btnGuardarActionPerformed

