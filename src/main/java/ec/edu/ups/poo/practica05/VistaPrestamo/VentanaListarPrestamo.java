    public void cargarDatos(){
        
        List<Prestamo> prestamos = prestamoControlador.list();
        DefaultTableModel tabla = (DefaultTableModel) tblPrestamo.getModel();
        tabla.setNumRows(0);
        for (Prestamo prestamo : prestamos) {
             SimpleDateFormat fecha = new SimpleDateFormat("dd/MM/yyyy");
           String fechaP= fecha.format(prestamo.getFechaPrestamo());
           String fechaD = fecha.format(prestamo.getFechaDevolucion());
            Object[] rowData = {prestamo.getNumeroPrestamo(), prestamo.getLibros(), prestamo.getUsuario().getNombre(), fechaP,
                fechaD};
            tabla.addRow(rowData);
        } 
    }
    public void cambiarIdioma(Locale locale) {
        ResourceBundle mensajes = ResourceBundle.getBundle("mensajes.mensajes", locale);
        String numeroPrestamo = mensajes.getString("lblNumPrestamo");
        String libros = mensajes.getString("libroMenu");
        String cliente = mensajes.getString("clienteMenu");
        String fechaPres = mensajes.getString("lblPrestamo");  
        String fechaDevol = mensajes.getString("lblDevolucion");               

        DefaultTableModel tabla = (DefaultTableModel) tblPrestamo.getModel();
        tabla.setColumnIdentifiers(new String [] {numeroPrestamo, libros,cliente,fechaDevol,fechaPres});
            this.setTitle(mensajes.getString("ListarPrestamo"));
        
        btnListar.setText(mensajes.getString("btnListar"));        
    }

