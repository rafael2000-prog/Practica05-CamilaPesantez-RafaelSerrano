    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        String identificacion = txtCedula.getText();
        String nombre = txtNombre.getText();
        String correo = txtCorreo.getText();
        Usuario usuario = usuarioControlador.read(identificacion);
        if(usuario != null){
            JOptionPane.showMessageDialog(this, "El usuario ya se ha registrado");
        }else{
            usuarioControlador.create(nombre, identificacion, correo);
            txtCedula.setText("");
            txtNombre.setText("");
            txtCorreo.setText("");
            JOptionPane.showMessageDialog(this, "Su registro fue existoso!");
        }
    }//GEN-LAST:event_btnGuardarActionPerformed
    public void cambiarIdioma(Locale locale){
        ResourceBundle mensajes = ResourceBundle.getBundle("mensajes.mensajes", locale);
        lblCedula.setText(mensajes.getString("lblCedula"));
        lblNombre.setText(mensajes.getString("lblNombre"));
        lblCorreo.setText(mensajes.getString("lblCorreo"));
        btnGuardar.setText(mensajes.getString("btnGuardar"));
        this.setTitle(mensajes.getString("RegistarUsuario"));

    }

